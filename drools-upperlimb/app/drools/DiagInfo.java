package drools;

public class DiagInfo {

    private String diag;
    private float muscComp1;
    private float muscComp2;
    private Boolean assist;

    public Boolean getAssist() {
        return assist;
    }

    public void setAssist(Boolean assist) {
        this.assist = assist;
    }

    public String getDiag() {
        return diag;
    }

    public void setDiag(String diag) {
        this.diag = diag;
    }

    public float getMuscComp1() {
        return muscComp1;
    }

    public void setMuscComp1(float muscComp1) {
        this.muscComp1 = muscComp1;
    }

    public float getMuscComp2() {
        return muscComp2;
    }

    public void setMuscComp2(float muscComp2) {
        this.muscComp2 = muscComp2;
    }



}